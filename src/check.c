#include "minishell.h"

/******************************************************************************
Double and simple quote check
Return : 0 = OK, other value NOK
******************************************************************************/
int	check_quote(char *s)
{
	int	count1;
	int	count2;

	count1 = 0;
	count2 = 0;
	while (*s != 0)
	{
		if (*s == '"')
		{
			count1++;
			s++;
			while (*s != '"' && *s != 0)
				s++;
			if (*s == '"')
				count1++;
		}
		if (*s == 39)
		{
			count2++;
			s++;
			while (*s != 39 && *s != 0)
				s++;
			if (*s == 39)
				count2++;
		}
		s++;
	}
	return (count1 % 2 + count2 % 2);
}
