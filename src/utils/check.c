#include "minishell.h"

/******************************************************************************
Double and simple quote check
Return : 0 = OK, other value NOK
******************************************************************************/
int	check_quote(char *s)
{
	int	count;

	while (*s != 0)
	{
		if (*s == '"')
		{
			count = 1;
			s++;
			while (*s != '"' && *s != 0)
				s++;
			if (*s == '"')
				count = 0;
		}
		if (*s == 39)
		{
			count = 1;
			s++;
			while (*s != 39 && *s != 0)
				s++;
			if (*s == 39)
				count = 0;
		}
		s++;
	}
	return (count);
}
